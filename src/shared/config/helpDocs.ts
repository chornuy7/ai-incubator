/**
 * Находит документацию по строке-топику и (опционально) ключу модуля.
 * Приоритет: точный псевдоним топика → подсказка moduleKey → вхождение подстроки.
 */
export function findHelpDoc(topic: string, moduleKey?: string): HelpDoc | null {
  const n = normalize(topic)

  // 1) Точное совпадение по заголовку модуля / уникальному псевдониму секции.
  if (n && ALIASES[n] && HELP_DOCS[ALIASES[n]]) return HELP_DOCS[ALIASES[n]]

  // 2) Подсказка из URL — надёжно разрешает секции внутри модуля
  //    (в т.ч. неоднозначные «Настройки поиска» / «Настройки парсинга»).
  if (moduleKey && HELP_DOCS[moduleKey]) return HELP_DOCS[moduleKey]

  // 3) Мягкое совпадение: топик содержит заголовок модуля или наоборот.
  if (n) {
    for (const [alias, key] of Object.entries(ALIASES)) {
      if (alias.length >= 4 && (n.includes(alias) || alias.includes(n)) && HELP_DOCS[key]) {
        return HELP_DOCS[key]
      }
    }
  }

  return null
}

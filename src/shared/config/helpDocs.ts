// ── Матчинг topic-строки к документации ──────────────────────────────────
// helpTopic приходит либо как заголовок модуля (ModuleConfig.title),
// либо как заголовок секции (из SectionCard). Дополнительно поддерживается
// подсказка moduleKey (например, из URL /panel/modules/:moduleKey), которая
// однозначно разрешает неоднозначные заголовки секций внутри модуля.

function normalize(s: string): string {
  return (s || '').toLowerCase().replace(/\s+/g, ' ').trim()
}

/** Уникальные псевдонимы (нормализованные) → ключ модуля. Неоднозначные
 * заголовки секций (напр. «Настройки поиска», «Настройки парсинга») сюда НЕ входят —
 * они разрешаются через moduleKey. */
const ALIASES: Record<string, string> = {}
function addAlias(key: string, ...aliases: string[]) {
  for (const a of aliases) {
    const n = normalize(a)
    if (n && !(n in ALIASES)) ALIASES[n] = key
  }
}

